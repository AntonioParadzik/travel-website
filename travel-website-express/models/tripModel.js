class Trip {
  constructor(
    id,
    heading,
    image,
    text,
    location1,
    location2,
    location3,
    price
  ) {
    this.id = id;
    this.heading = heading;
    this.image = image;
    this.text = text;
    this.location1 = location1;
    this.location2 = location2;
    this.location3 = location3;
    this.price = price;
  }
}

export default Trip;
